    public void tampilData() {
    try {
        model = (DefaultTableModel) JTabel1.getModel(); // 🔥 ini yang bener
        model.setRowCount(0);

        st = conn.createStatement();
        rs = st.executeQuery("SELECT * FROM datamhs");

        while (rs.next()) {
            Object[] row = {
                rs.getString("nim"),
                rs.getString("nama"),
                rs.getString("semester"),
                rs.getString("kelas")
            };
            model.addRow(row);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal tampil: " + e.getMessage());
    }
}
