    private void simpanData() {
    try {
        String sql = "INSERT INTO datamhs VALUES ('"
                + jTextField1.getText() + "','"
                + jTextField2.getText() + "',"
                + jTextField3.getText() + ",'"
                + jTextField4.getText() + "')";
        
        st = conn.createStatement();
        st.executeUpdate(sql);
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
        tampilData();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal simpan: " + e.getMessage());
    }
}
    
    private void updateData() {
    try {
        String sql = "UPDATE datamhs SET "
                + "nama='" + jTextField2.getText() + "', "
                + "semester=" + jTextField3.getText() + ", "
                + "kelas='" + jTextField4.getText() + "' "
                + "WHERE nim='" + jTextField1.getText() + "'";

        st = conn.createStatement();
        st.executeUpdate(sql);
        JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
        tampilData();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal update: " + e.getMessage());
    }
}
    
    private void hapusData() {
    try {
        String sql = "DELETE FROM datamhs WHERE nim='"
                + jTextField1.getText() + "'";

        st = conn.createStatement();
        st.executeUpdate(sql);
        JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
        tampilData();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal hapus: " + e.getMessage());
    }
}
    
